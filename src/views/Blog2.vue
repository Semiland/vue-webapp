                                <p>The range is still one of the biggest obstacles for customers before buying an electric vehicle. The automotive supplier Bosch now intends to counteract this with the use of silicon carbide semiconductor components: The higher efficiency of these components compared with silicon transistors would extend their range by 6 %, explained Harald Kröger, Bosch Board of Management member. Because these components can withstand higher operating temperatures than their silicon counterparts, less effort is required to cool them. According to Kröger, this in turn has a positive effect on the weight and costs of the power systems.  According to a company press release, Bosch has established a pilot production line for SiC components at its semiconductor plant in Reutlingen. In 2020, first samples will be delivered to customers. Series production will then be started depending on customer’s approval, a company spokesperson explained. The production line in Reutlingen uses wafers with diameters of 150- and 200-mm. For the SiC technology, the company has invested a triple-digit million euros amount. Bosch intends to use the SiC components from its production for use in its captive market of electronic vehicle systems. The company is the only automotive supplier that also manufactures semiconductors. In addition to the power components, these are mainly MEMS sensors and ASICs. At its second production site in Dresden, which is scheduled to start operations in spring 2020, Bosch will manufacture on 300-mm wafers. Already this year, the first 200 employees will start their work in the new wafer factory in the immediate vicinity of Globalfoundries. The fabs in Reutlingen and Dresden will complement each other ideally, Kröger said.</p>
